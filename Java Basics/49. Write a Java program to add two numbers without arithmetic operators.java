// 49. Write a Java program to add two numbers without arithmetic operators.

public class practice{
    public static void main(String args[]){
        
        int sum = 0;
        
        int num1 = 10;
        
        int num2 = 14;
        
        for(int i = 1; i<=num1;i++){
            sum++;
        }
        
        for(int i =1; i<=num2;i++){
            sum++;
        }
        
        System.out.println("Sum : "+sum);
    }
}
